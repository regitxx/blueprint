// Read GEMINI_API_KEY from local.properties (gitignored) if present, so it never enters git.
// AI Studio / CI can also inject it via the GEMINI_API_KEY environment variable.
val geminiKey: String = run {
    val p = Properties()
    val f = rootProject.file("local.properties")
    if (f.exists()) f.inputStream().use { p.load(it) }
    p.getProperty("GEMINI_API_KEY") ?: System.getenv("GEMINI_API_KEY") ?: ""
}

